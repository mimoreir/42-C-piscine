int	ft_any(char **tab, int (*f) (char*))
{
	int	i;
	int	j;
	int	count;

	i = 0;
	j = 0;
	count = 0;
	while (tab[i])
	{
		if (f(tab[i]))
			count++;
		i++;
	}
	if (count > 0)
		return (1);
	return (0);
}
